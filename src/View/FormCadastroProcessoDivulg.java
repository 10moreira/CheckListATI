private void atualizaGrid(){
     String ejbql = "from ProcessoDivul";
         String ordem = "";
         if(JcbxOrdenar.getSelectedItem().toString().equals("Tipo Divulgação")){
             ordem = "tipoDivulg";
         }else if(JcbxOrdenar.getSelectedItem().toString().equals("Convite")){
             ordem = "convite";
         }
         String filtro = jTextFieldFiltro.getText();
         if(filtro.length() > 0 && filtro != null ){
             ejbql += " where upper("+ordem+") like'"+filtro.toUpperCase()+"%' ";
         }
         ejbql += " order by " + ordem;
        System.out.println("EJBQL gerado: " + ejbql);
        query = entityManager.createQuery(ejbql);
        list.clear();
        list.addAll(query.getResultList());
        int row = list.size() - 1;
        jTableProceDivul2.setRowSelectionInterval(row, row);
        jTableProceDivul2.scrollRectToVisible(jTableProceDivul2.getCellRect(row, row, true));
}
    
