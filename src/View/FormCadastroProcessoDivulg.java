    private void jButtonExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonExcluirActionPerformed
                        
          if (Utilitarias.Uteis.confirmar("Você deseja excluir?")) {
            int linhaClicada = jTableProceDivul2.getSelectedRow();
            if (linhaClicada < 0) {
                JOptionPane.showMessageDialog(null, "Selecione um registro para a excluão!");
            } else {
                try {

                    ProcessoDivul obj = list.get(linhaClicada);
                    entityManager.getTransaction().begin();
                    entityManager.remove(obj);
                    entityManager.getTransaction().commit();
                    editando = false;
                    trataEdicao();
                    atualizaGrid();
                } catch (Exception e) {
                    JOptionPane.showMessageDialog(null, "Erro ao gravar" + e.getMessage());
                    if (entityManager.getTransaction().isActive() == false) {
                        entityManager.getTransaction().begin();
                    }
                    entityManager.getTransaction().rollback();
                    editando = false;
                    atualizaGrid();
                    trataEdicao();
                }
            }
        }
    }//GEN-LAST:event_jButtonExcluirActionPerformed

    private void jButtonGravarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonGravarActionPerformed
                if(validaCampos()){
                        try{
                                int linhaClicada = jTableProceDivul2.getSelectedRow();
                                ProcessoDivul obj = list.get(linhaClicada);
                                entityManager.getTransaction().begin();
                                entityManager.persist(obj);
                                entityManager.getTransaction().commit();
                                editando = false;
                                trataEdicao();
                                atualizaGrid();
                            }
                        catch(Exception e){
                                JOptionPane.showMessageDialog(null, "Erro ao gravar" + e.getMessage());
                                if(entityManager.getTransaction().isActive()==false){
                                        entityManager.getTransaction().begin();
                                    }
                                entityManager.getTransaction().rollback();
                                editando = false;
                                atualizaGrid();
                                trataEdicao();
                            }
                    }
    }//GEN-LAST:event_jButtonGravarActionPerformed

